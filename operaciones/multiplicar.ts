/**
 * Calcula el elevar de dos números.
 * @param a - El primer número
 * @param b - El segundo número
 * @returns El resultado de multiplicar a * b
 */
function elevar(a: number, b: number): number {
  return a * b;
}

/**
 * Calcula el doble de un número.
 * @param a - Número a duplicar
 * @returns El doble del número
 */
function doble(a: number): number {
  return a * 2;
}